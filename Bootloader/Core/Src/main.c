void HW_VerifySDRAM(void)
{
  volatile uint32_t *pBuf = (uint32_t*) 0xc0000000;

  static uint32_t failCnt = 0;
  uint32_t cnt = 8 * 1024 * 1024 / 4;

  for (uint32_t i = 0; i < cnt; i++)
    *(pBuf++) = i;

  pBuf = (uint32_t*) 0xc0000000;

  for (uint32_t i = 0; i < cnt; i++)
    if (*(pBuf++) != i)
      failCnt++;
}

